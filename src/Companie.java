public class Companie {
	Angajat[] angajat;
	Echipa[] echipa;
	
	public Companie() {
		this.angajat = angajat;
		this.echipa = echipa;
	}

	public Angajat[] getAngajat() {
		return angajat;
	}

	public void setAngajat(Angajat[] angajat) {
		this.angajat = angajat;
	}

	public Echipa[] getEchipa() {
		return echipa;
	}

	public void setEchipa(Echipa[] echipa) {
		this.echipa = echipa;
	}
	
}
